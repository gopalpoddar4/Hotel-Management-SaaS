import com.nexifotech.hotelsaas.feature.reports.domain.model.InsightType
import com.nexifotech.hotelsaas.feature.reports.domain.model.ReportInsight

@Composable
fun InsightCard(
    insight: ReportInsight,
    modifier: Modifier = Modifier
) {
    val (icon, color, bgColor) = when (insight.type) {
        InsightType.HIGHLIGHT -> Triple(Icons.Default.Star, Color(0xFFF57C00), Color(0xFFFFF3E0))
        InsightType.NOTE -> Triple(Icons.Default.Info, Color(0xFF1976D2), Color(0xFFE3F2FD))
        InsightType.RECOMMENDATION -> Triple(Icons.Default.Lightbulb, Color(0xFF388E3C), Color(0xFFE8F5E9))
    }

    Card(
        modifier = modifier.fillMaxWidth(),
        shape = RoundedCornerShape(12.dp),
        colors = CardDefaults.cardColors(
            containerColor = MaterialTheme.colorScheme.surface
        ),
        elevation = CardDefaults.cardElevation(defaultElevation = 1.dp)
    ) {
        Row(
            modifier = Modifier
                .fillMaxWidth()
                .padding(16.dp),
            verticalAlignment = Alignment.Top
        ) {
            Box(
                modifier = Modifier
                    .size(40.dp)
                    .clip(CircleShape)
                    .background(bgColor),
                contentAlignment = Alignment.Center
            ) {
                Icon(
                    imageVector = icon,
                    contentDescription = null,
                    tint = color,
                    modifier = Modifier.size(20.dp)
                )
            }
            Spacer(modifier = Modifier.width(16.dp))
            Column {
                Text(
                    text = insight.title,
                    style = MaterialTheme.typography.titleSmall,
                    fontWeight = FontWeight.SemiBold,
                    color = MaterialTheme.colorScheme.onSurface
                )
                Spacer(modifier = Modifier.height(4.dp))
                Text(
                    text = insight.description,
                    style = MaterialTheme.typography.bodySmall,
                    color = MaterialTheme.colorScheme.onSurfaceVariant
                )
            }
        }
    }
}
